local logs = "https://discord.com/api/webhooks/883422021403426868/IRTBWolaW7CCx-CEkVW_uHzoNI8WMLJFHdHqMU6y6ogSfuDkfrPO_1Fvv-5vd6ZbuSSq"
local communityname = "Department of Colorado Roleplay"
local communtiylogo = "https://cdn.discordapp.com/attachments/883422499860250645/883422704907202661/static.png" --Must end with .png

AddEventHandler('playerConnecting', function()
local name = GetPlayerName(source)
local ip = GetPlayerEndpoint(source)
local ping = GetPlayerPing(source)
local steamhex = GetPlayerIdentifier(source)
local connect = {
        {
            ["color"] = "8663711",
            ["title"] = "Player Connected to ColoradoRP Server 1",
            ["description"] = "Player: **"..name.."**\nIP: **"..ip.."**\n Steam Hex: **"..steamhex.."**",
	        ["footer"] = {
                ["text"] = communityname,
                ["icon_url"] = communtiylogo,
            },
        }
    }

PerformHttpRequest(logs, function(err, text, headers) end, 'POST', json.encode({username = "ColoradoRP FiveM User Logger", embeds = connect}), { ['Content-Type'] = 'application/json' })
end)

